// Helper functions for player data
export const normalizeName = (name: string): string => {
  const nameAliases: Record<string, string> = { 
    'jt miller': 'j.t. miller', 
    'tim stutzle': 'tim st√ºtzle', 
    'jj peterka': 'jj peterka', 
    'adam fantili': 'adam fantilli' 
  }
  const normalized = name.toLowerCase().replace(/[^\w\s]/g, '').replace(/\s+/g, ' ').trim()
  return nameAliases[normalized] || normalized
}

export const playersByName = new Map(
  playerRankings.map(p => [normalizeName(p.name), p])
)

export const findPlayerByName = (inputName: string): Player | undefined => {
  return inputName ? playersByName.get(normalizeName(inputName)) : undefined
}

export const getPlayerPrimaryPosition = (player: Player): string => {
  if (!player || !player.pos) return 'C'
  if (player.pos.includes('G')) return 'G'
  if (player.pos.includes('D')) return 'D'
  if (player.pos.includes('C')) return 'C'
  if (player.pos.includes('LW')) return 'LW'
  if (player.pos.includes('RW')) return 'RW'
  return 'C' // Default fallback
}
